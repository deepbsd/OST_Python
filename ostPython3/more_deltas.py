#!/usr/bin/env python3

from datetime import datetime, timedelta

weeks = timedelta(weeks=2)
hours = timedelta(hours=1)
minutes = timedelta(minutes=100)
seconds = timedelta(seconds=1000)
composite = timedelta(hours=1, minutes=30)

now = datetime.now()
print("Now:", now)
print("Now + 2 weeks:", now + weeks)
print("Now + 1 hour:", now + hours)
print("Now + 100 minutes:", now + minutes)
print("Now + 1000 seconds:", now + seconds)
print("Now + composite (1h:30min):", now + composite)
